    void validarUsuario(String usuario, String clave)
    {
        try{
            Login login = new Login();
            Usuario usu = new Usuario();
            usu.setUsuario(usuario);
            usu.setClave(clave);
            
            usuarioDAO.validarUsuario(usu);
            
            if(usu.getPrimerNombreUsuario() != null)
            {
                JOptionPane.showMessageDialog(null, "Bienvenida, "+usu.getPrimerNombreUsuario());
                this.dispose();
                login.setVisible(true);
            }else
            {
                JOptionPane.showMessageDialog(null, "Usuario y/o clave incorrectos");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error : "+e);
        }
            
    }
    
    
